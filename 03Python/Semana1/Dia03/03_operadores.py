# Operadores aritméticos
a = 5
b = 2
print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)

# Operadores lógicos y de comparación
x = 10
y = 20
print("Mayor que:", x > y)
print("Menor que:", x < y)
print("Igual a:", x == y)
print("Diferente de:", x != y)
print("AND lógico:", (x > 5) and (y < 30))
print("OR lógico:", (x > 5) or (y < 10))
